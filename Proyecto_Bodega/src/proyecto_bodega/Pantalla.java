    private void tabla_vistaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabla_vistaMouseClicked
        // TODO add your handling code here:
        click_tabla = this.tabla_vista.rowAtPoint(evt.getPoint());
        
        String NombreProducto = ""+tabla_vista.getValueAt(click_tabla, 0);
        String Tipo_producto = ""+tabla_vista.getValueAt(click_tabla, 1);
        String Marca = ""+tabla_vista.getValueAt(click_tabla, 2);
        double Peso = (double)tabla_vista.getValueAt(click_tabla, 3);
        double Precio = (double)tabla_vista.getValueAt(click_tabla, 4);
        int x=0;
        int column = tabla_vista.getColumnModel().getColumnIndexAtX(evt.getX());
        int Row = evt.getY()/tabla_vista.getRowHeight();
        
        if (Row<tabla_vista.getRowCount() && Row>=0 & column<tabla_vista.getColumnCount() && column>=0) {
            Object value = tabla_vista.getValueAt(Row, column);
            if (value instanceof  JButton) {
                ((JButton)value).doClick();
                JButton button = (JButton) value;
                
                if (button.getName().equals("M")) {
                   mod.setVisible(true);
                   pd.setVisible(false);
                    if (Tipo_producto.equals("Molido")) {
                        x=1;
                    }else if (Tipo_producto.equals("Paca")) {
                        x=2;
                    }else if (Tipo_producto.equals("Grano")) {
                        x=3;
                    }else if (Tipo_producto.equals("enlatado")) {
                        x=3;
                    }else if (Tipo_producto.equals("enbotellado")) {
                        x=3;
                    }else if (Tipo_producto.equals("enpaquetado")) {
                        x=3;
                    }else if (Tipo_producto.equals("Fierro nuevo")) {
                        x=3;
                    }
                   mod.insertar_datos(NombreProducto,x, Marca, Peso, Precio);
                   
                }
                if (button.getName().equals("E")) {
                    mod.setVisible(false);
                    pd.setVisible(false);
                    int n = JOptionPane.showConfirmDialog(  
                    null,
                    "Seguro que quieres eliminar este registro?!\nnombre Producto: " + NombreProducto ,
                    "",
                    JOptionPane.YES_NO_OPTION);
                    
                    if(n == JOptionPane.YES_OPTION)
                    {
                        Conexion.Conexion.Eliminar_Producto("Delete from productos where Nombre='"+NombreProducto+"';");
                        actualizar_tabla();
                        //JOptionPane.showMessageDialog(null, "registro Eliminado");
                    }
            } 
                if (button.getName().equals("P")) {
                        mod.setVisible(false);
                        pd.setVisible(true);
                        pd.insertar_datos(NombreProducto, Tipo_producto, Marca, Peso, Precio);
                    }
        }
    }//GEN-LAST:event_tabla_vistaMouseClicked
 }
