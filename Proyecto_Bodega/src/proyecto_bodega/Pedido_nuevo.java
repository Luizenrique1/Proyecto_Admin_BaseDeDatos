    private void btn_ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ActualizarActionPerformed
        // TODO add your handling code here:
        if (cantidad>= (int)PE_Cantidad.getValue()) {
            boolean val =true;
        if (!isValidName(NP_NT.getText())) {
            val=false;
        }
        if (!val) {
             JOptionPane.showMessageDialog(this,"Campo invalido o vacia","Error",JOptionPane.WARNING_MESSAGE);
        }else{
        if (PE_Cantidad.getValue().equals(0) || NP_Calle.getText().equals("") || NP_NUMCASa.getText().equals("") || NP_NT.getText().equals("")) {
             JOptionPane.showMessageDialog(this,"Campo invalido o vacia","Error",JOptionPane.WARNING_MESSAGE);
        }else{
            try {
               this.setVisible(false);
               Conexion.Conexion.Ejecutar("execute InsertarPedido "+PE_Cantidad.getValue()+",'"+NP_Calle.getText() +"','"+ NP_NUMCASa.getText()+"','"+NP_NT.getText()+"',0,'','"+Md_Nombre_producto.getText()+"',0,''" );
            
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this,"Error al insertar datos","Error",JOptionPane.WARNING_MESSAGE);
            }

        }
        
    }
        }else{
         JOptionPane.showMessageDialog(this,"Bodega no contiene Cantidad suficiente para Pedido","Error",JOptionPane.WARNING_MESSAGE);
        
        }

    }//GEN-LAST:event_btn_ActualizarActionPerformed

